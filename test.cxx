#include <iostream>
#include <string>
#include <bits/stdc++.h>

using namespace std;

int main(){ 
    int x = 0;
    int y = 1;
    cout << "!x = " <<!x;
    cout <<"\n";
    cout << "x && y = " << x&&y;
    cout << "\n";
    cout << "x || y = " << x||y;
    cout <<"\n";

    printf("!x = %d\n" ,!x);
    printf("x && y = %d\n", x&&y);
    printf("x || y = %d\n", x||y);

    return 0;
}